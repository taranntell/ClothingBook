    enum Item{
        case bhtshirt, pants, shoes, hat, blouseshirt, socks, dresssuit, ring, bh, glasses, notes
        func sizes(_ sex:SizeModel.Sex, width hasWidth:Bool = false, length hasLength:Bool = false) -> [String] {
            switch self {
            case .blouseshirt:
                return [defaultEmptyValue,"NA"]
            case .bhtshirt:
                return [defaultEmptyValue,"NA"]
            case .pants:
                if hasWidth { return [defaultEmptyValue,"NA"] }
                else if hasLength{ return [defaultEmptyValue,"NA"] }
                return []
            case .shoes:
                return [defaultEmptyValue,"NA"]
            case .hat:
                return [defaultEmptyValue,"NA"]
            case .socks:
                return [defaultEmptyValue,"NA"]
            case .dresssuit:
                return [defaultEmptyValue,"NA"]
            case .ring:
                return [defaultEmptyValue,"NA"]
            case .bh:
                return [defaultEmptyValue,"NA"]
            case .notes:
                return []
            case .glasses:
                return []
            }
        }
        
        func name(_ sex:Sex) -> String {
            let isGuy = (sex == Sex.Guy) ? true : false
            switch self {
            
            case .pants:
                return "Pants"
            case .shoes:
                return "Shoes"
            case .hat:
                return "Hat"
            case .blouseshirt:
                return !isGuy ? "Blouse" : "Shirt"
            case .bhtshirt:
                return !isGuy ? "BH" : "T-Shirt"
                
            case .socks:
                return "Socks"
            case .dresssuit:
                return !isGuy ?"Dress" : "Suit"
            case .ring:
                return "Ring"
            case .bh:
                return "BH"
            case .notes:
                return "Notes"
            case .glasses:
                return "Glasses"
            }
        }

